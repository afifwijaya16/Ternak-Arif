                        <h3>Alasan Memulai Ternak.in</h3><br>
                        <p>
                            Ternak dan hasil produksinya merupakan sumber bahan pangan protein yang sangat penting untuk peningkatan kualitas sumber daya manusia Indonesia. Perkembangan populasi ternak utama dan hasil produksinya merupakan gambaran tingkat ketersediaan sumber bahan protein nasional. Tingkat konsumsi yang akan menentukan kualitas sumber daya manusia dipengaruhi oleh tingkat ketersediaan daging dan produksi ternak lainnya serta tingkat pendapatan rumah tangga (purchasing power).
                            Pemerintah Indonesia pun sebenernya sudah turut memberikan bantuan dengan beberapa program yang akan membantu para peternak kedepannya seperti halnya Kemenhub yang sudah menyiapkan dana sebesar Rp.100 miliar untuk membangun sebuah kapal angkutan untuk ternak  (SINDONEWS.com) yang menurut kami dapat memberikan konstrubusi yang baik guna menekan biaya angkutan saat ini, sehingga memberikan keuntungan yang lebih besar lagi kepada para peternak.

                        </p>
                        <p>
                            Akan tetapi bedasarkan data konsumsi di indonesia daging, telur dan susu menunjukan peningkatan. Konsumsi daging sapi pada tahun 2016 meningkat sebesar 3,21% dibandingkan tahun sebelumya, konsumsi telur ayam ras meningkat sebesar 2,46% dan konsumsi susu olahan cair meninkat sebesar 24,43% (Pusat Data dan Sistem Informasi Pertanian Kementrian Pertanian Republik Indonesia, 2017). Dengan jumlah data tersebut dapat dipastikan bahwasannya setiap tahunnya permintaan akan hasil produksi ternak terus mengalami peningkatan akan tetapi kemampuan peternak lokal dalam mencukupi permintaan konsumsi dinilai masih belum cukup. Terbukti dengan data nilai impor telur, susu dan daging yang terus meningkat. Sehingga pendapatan usaha peternak lokal pun terus mengalami penyusutan.
                            Sementara disisi wirausaha Indonesia mengalami peningkatan, seperti yang disampaikan oleh Mentri Koprasi dan Usaha Kecil Menengah Anak Agung Gede Ngurah Puspayoga pada acara Gerakan Kewirausahaan Nasiaonal di IPB Bogor, berdasarkan data Badan Pusat Statistik ratio wirausaha Indonesia naik menjadi 3,1 persen dibandingkan dengan taun 2013/2014 lalu yang masih 1,67 persen.  (Humas Kementrian Kopersai dan UKM, 2017) dengan ini dapat disimpulkan bahwa setiap tahunnya penggiat wirusaha di Indonesia akan terus meningkat akan tetapi masalah utama yang akan dialami oleh para wirausaha ini adalah kurang nya lahan
                        </p>
